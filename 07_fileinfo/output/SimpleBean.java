package com.example.bean;

/**
 * SimpleBean。
 * Gradle로 자동 생성하고 있다
 */
public class SimpleBean {

    private String name;
    private int value;

    public void SimpleBean() {}

    public String getName() {
    	return name;
    }
    public void setName(String name) {
    	this.name = name;
    }
    public int getValue() {
    	return value;
    }
    public void setValue(int value) {
    	this.value = value;
    }    
}